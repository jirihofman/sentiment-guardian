'use client';

import React, { useState } from 'react';
import { Button, Modal } from 'react-bootstrap'; 
import { ChatDotsFill } from 'react-bootstrap-icons';
import PropTypes from 'prop-types';

const CarouselCommentary = ({ comments, model }) => {

    const [show, setShow] = useState(false);

    const handleClose = () => setShow(false);
    const handleShow = () => setShow(true);

    const cardBodyStyle = {
        maxHeight: '120px',
        minHeight: '120px',
        overflow: 'hidden',
    };

    return (
        <div className='mx-0 p-0'>
            <div className="row row-cols-1 row-cols-1 g-2 mb-0">
                <div className="col">
                    <div className="card h-100">
                        {/* The `style` is for Safari maxHeight audio hack */}
                        <div className="card-header" style={{ maxHeight: '41px' }}>
                            <span className="d-none d-lg-block">
                                Commentary
                                <Button variant="secondary" title='Click to see the full comment.' onClick={handleShow} size='sm' style={{ marginLeft: '105px', marginRight:'5px', marginTop: '-16px' }}>
                                    <ChatDotsFill style={{ marginBottom: '5px' }} />
                                </Button>
                                <audio controls src={comments.audio} style={{ display: 'inline', maxHeight: '24px', overflow: 'hidden', width: '180px' }} />
                            </span>
                            <span className="d-lg-none">
                                Commentary
                                <Button variant="secondary" title='Click to see the full comment.' onClick={handleShow} size='sm' style={{ marginLeft: '5px', marginRight: '5px', marginTop: '-16px' }}>
                                    <ChatDotsFill style={{ marginBottom: '5px' }} />
                                </Button>
                                <audio controls src={comments.audio} style={{ display: 'inline', maxHeight: '24px', overflow: 'hidden', width: '100px' }}/>
                            </span>
                        </div>
                        <div className="card-body " style={cardBodyStyle}>
                            {/* Display full comment on large screens */}
                            <cite title="Our AI says">
                                <div className="d-none d-lg-block px-5 mx-5">{comments.comment}</div>
                                <div className="d-lg-none px-3">{comments.comment.slice(0, 80)}...</div>
                            </cite>

                            <Modal show={show} onHide={handleClose}>
                                <Modal.Header closeButton>
                                    <Modal.Title>Full Comment</Modal.Title>
                                </Modal.Header>
                                <Modal.Body>
                                    {comments.comment}
                                </Modal.Body>
                                <Modal.Footer>
                                    <caption className="blockquote-footer">Generated by {model} on {comments.date}</caption>
                                    <Button variant="secondary" onClick={handleClose}>
                                        Close
                                    </Button>
                                </Modal.Footer>
                            </Modal>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    );
};

CarouselCommentary.propTypes = {
    comments: PropTypes.object.isRequired,
    model: PropTypes.string.isRequired,
};

export default CarouselCommentary;
